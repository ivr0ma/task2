#include <stdio.h>



/*===================================*/
/*--------------Main-----------------*/
/*===================================*/
int main()
{
  double x, a,
         zn=0;                  /*значение многочлена в точке х*/
  if (scanf("%lf", &x) == 0)    /*проверка на пустой ввод*/
    return 0;
  while (scanf("%lf", &a) == 1)
  {
    zn = zn*x + a;
  }
  printf("%.10g\n", zn);         /*полагаю, что для пустого многочлена его значение равно 0*/
}
